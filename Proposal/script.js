class Proposal {
  constructor(data, callbacks) {
    this.data = data;
    this.callbacks = callbacks;

    this.domElement = {
      list: {},
      details: {}
    }
    this.handleClick = this.handleClick.bind(this)
  }
  renderRow() {
    var row = this.domElement.list.row = $("<div>", {
      class: "row",
      click: this.handleClick
    })
    var teacherName = this.domElement.list = $("<div>", {
      class: "col-3",
      text: this.data.teacherName
    })
    var grade = this.domElement.list = $("<div>", {
      class: "col-2",
      text: this.data.gradeLevel.name
    })
    var title = this.domElement.list = $("<div>", {
      class: "col-5",
      text: this.data.title
    })
    var funded = this.domElement.list = $("<div>", {
      class: "col-2",
      text: this.data.percentFunded
    })

    row.append(teacherName, grade, title, funded);
    return row;

  }
  renderDetail() {
    var card = $("<div>", {
      class: "card",
      style: `width:500px`,
    })
    var img = $("<img>", {
      class: "card-img-top",
      src: this.data.retinaImageURL,
      alt: `Card Image`,
    })
    var cardImg = $("<div>", {
      class: "card-img-overlay"
    })
    var cardTitle = $("<h4>", {
      class: "card-title",
      text: this.data.fulfillmentTrailer,
    })
    var cardText = $("<p>", {
      class: "card-text card-text-scrollable",
      text: this.data.shortDescription
    })
    var fundURL = $("<a>", {
      class: `btn btn-sm btn-primary`,
      text: `Donate Now`,
      href: this.data.fundURL
    })
    var proposalURL = $("<a>", {
      class: `btn btn-sm btn-info`,
      text: `Read Proposal`,
      href: this.data.proposalURL
    })
    var schoolUrl = $("<a>", {
      class: `btn btn-sm btn-light`,
      text: `See School`,
      href: this.data.schoolUrl
    })
    cardImg.append(cardTitle, cardText, fundURL, proposalURL, schoolUrl)
    card.append(img, cardImg);
    return card;
  }
  handleClick(e) {
    if (e) e.preventDefault();
    this.callbacks.click(this);
  }
}

class ProposalList {
  constructor(elementConfig) {
    this.dom = {
      itemArea: $(elementConfig.itemArea),
      detailArea: $(elementConfig.detailArea),
      nav: {
        navgationBar: $(elementConfig.navgationBar)
      }
    }

    this.handleProposalClick = this.handleProposalClick.bind(this)
    this.changePage = this.changePage.bind(this);

    this.nav = new NavigationBar({
      click: this.changePage
    });
    
    this.proposals = [];
  }

  changePage(list) {
    this.nav.changePage(list);
  }

  addProposal(pdOne) {
    this.proposals.push(new Proposal(pdOne, {
      click: this.handleProposalClick
    }))
  }
  loadProposals(pd) {
    pd.forEach(v => { return this.addProposal(v) })
  }
  renderProposals(pd) {
    var pdRender = pd.map(v => {
      return v.renderRow();
    })
    this.dom.itemArea.empty().append(pdRender);
  }

  loadMenu() {
    this.dom.nav.navgationBar.append(this.nav.loadMenuItems())
  }

  displayAllProposals() {
    this.renderProposals(this.proposals);
  }
  handleProposalClick(pdOne) {
    var oneRender = pdOne.renderDetail();
    this.dom.detailArea.empty().append(oneRender);
  }
}


